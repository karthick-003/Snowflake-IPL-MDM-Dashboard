-- ============================================================================
-- 3. SILVER LAYER: TRANFORMATION & CLEANING
-- ============================================================================

USE SCHEMA SILVER;

CREATE OR REPLACE TABLE IPL_MDM.SILVER.CLEAN_DELIVERIES AS 
SELECT 
    MATCH_ID, 
    INNING, 
    BATTING_TEAM, 
    BOWLING_TEAM, 
    OVER + 1 AS OVER_NUMBER, 
    BALL, 
    BATTER, 
    BOWLER, 
    NON_STRIKER, 
    BATSMAN_RUNS, 
    EXTRA_RUNS, 
    TOTAL_RUNS, 
    CASE WHEN EXTRAS_TYPE IS NULL OR EXTRAS_TYPE = '' THEN 'NO_EXTRA' ELSE UPPER(EXTRAS_TYPE) END AS EXTRAS_TYPE, 
    IS_WICKET, 
    CASE WHEN PLAYER_DISMISSED IS NULL THEN 'NOT_DISMISSED' ELSE PLAYER_DISMISSED END AS PLAYER_DISMISSED, 
    CASE WHEN DISMISSAL_KIND IS NULL THEN 'NOT_DISMISSED' ELSE UPPER(DISMISSAL_KIND) END AS DISMISSAL_KIND, 
    CASE WHEN FIELDER IS NULL THEN 'NOT_APPLICABLE' ELSE FIELDER END AS FIELDER, 
    CASE WHEN EXTRA_RUNS > 0 THEN 'YES' ELSE 'NO' END AS IS_EXTRA, 
    CASE 
        WHEN BATTING_TEAM = 'Deccan Chargers' THEN 'Sunrisers Hyderabad' 
        WHEN BATTING_TEAM = 'Delhi Daredevils' THEN 'Delhi Capitals' 
        WHEN BATTING_TEAM = 'Kings XI Punjab' THEN 'Punjab Kings' 
        WHEN BATTING_TEAM = 'Rising Pune Supergiant' THEN 'Rising Pune Supergiants' 
        ELSE BATTING_TEAM 
    END AS BATTING_TEAM_STANDARD, 
    CASE 
        WHEN BOWLING_TEAM = 'Deccan Chargers' THEN 'Sunrisers Hyderabad' 
        WHEN BOWLING_TEAM = 'Delhi Daredevils' THEN 'Delhi Capitals' 
        WHEN BOWLING_TEAM = 'Kings XI Punjab' THEN 'Punjab Kings' 
        WHEN BOWLING_TEAM = 'Rising Pune Supergiant' THEN 'Rising Pune Supergiants' 
        ELSE BOWLING_TEAM 
    END AS BOWLING_TEAM_STANDARD, 
    CONCAT(MATCH_ID, '-', INNING, '-', OVER + 1, '-', BALL) AS BALL_ID
FROM IPL_MDM.BRONZE.RAW_DELIVERIES; 

-- Verification Queries for Silver Layer
SELECT COUNT(*) AS SILVER_ROWS FROM IPL_MDM.SILVER.CLEAN_DELIVERIES;
SELECT * FROM IPL_MDM.SILVER.CLEAN_DELIVERIES LIMIT 10;

SELECT EXTRAS_TYPE, COUNT(*) AS COUNT
FROM IPL_MDM.SILVER.CLEAN_DELIVERIES
GROUP BY EXTRAS_TYPE
ORDER BY COUNT DESC;

-- ============================================================================
-- 4. GOLD LAYER: PERFORMANCE SUMMARIES
-- ============================================================================
USE SCHEMA GOLD;

--GOLD TABLE 1 - TEAM PERFORMANCE SUMMARY
CREATE OR REPLACE TABLE IPL_MDM.GOLD.TEAM_PERFORMANCE AS
SELECT
    BATTING_TEAM_STANDARD          AS TEAM,
    COUNT(DISTINCT MATCH_ID)       AS MATCHES_PLAYED,
    SUM(BATSMAN_RUNS)              AS TOTAL_BATSMAN_RUNS,
    SUM(EXTRA_RUNS)                AS TOTAL_EXTRA_RUNS,
    SUM(TOTAL_RUNS)                AS TOTAL_RUNS_SCORED,
    SUM(IS_WICKET)                 AS TOTAL_WICKETS_LOST,
    ROUND(SUM(TOTAL_RUNS) / NULLIF(COUNT(DISTINCT MATCH_ID), 0), 2) AS AVG_RUNS_PER_MATCH
FROM IPL_MDM.SILVER.CLEAN_DELIVERIES
GROUP BY BATTING_TEAM_STANDARD
ORDER BY TOTAL_RUNS_SCORED DESC;


-- GOLD TABLE 2 - BOWLER PERFORMANCE SUMMARY
CREATE OR REPLACE TABLE IPL_MDM.GOLD.BOWLER_PERFORMANCE AS
SELECT
    BOWLER,
    COUNT(DISTINCT MATCH_ID)  AS MATCHES_PLAYED,
    COUNT(*)                  AS BALLS_BOWLED,
    SUM(TOTAL_RUNS)           AS RUNS_CONCEDED,
    SUM(IS_WICKET)            AS WICKETS_TAKEN,
    ROUND(SUM(TOTAL_RUNS) / NULLIF(SUM(IS_WICKET), 0), 2) AS BOWLING_AVERAGE,
    ROUND(COUNT(*) / NULLIF(SUM(IS_WICKET), 0), 2)        AS STRIKE_RATE
FROM IPL_MDM.SILVER.CLEAN_DELIVERIES
GROUP BY BOWLER
ORDER BY WICKETS_TAKEN DESC;


-- GOLD TABLE 3 - BATTER PERFORMANCE SUMMARY
CREATE OR REPLACE TABLE IPL_MDM.GOLD.BATTER_PERFORMANCE AS
SELECT
    BATTER,
    COUNT(DISTINCT MATCH_ID)       AS MATCHES_PLAYED,
    SUM(BATSMAN_RUNS)              AS TOTAL_RUNS,
    COUNT(*)                       AS BALLS_FACED,
    SUM(IS_WICKET)                 AS TIMES_DISMISSED,
    ROUND(SUM(BATSMAN_RUNS) / NULLIF(SUM(IS_WICKET), 0), 2)        AS BATTING_AVERAGE,
    ROUND((SUM(BATSMAN_RUNS) / NULLIF(COUNT(*), 0)) * 100, 2)      AS STRIKE_RATE,
    MAX(BATSMAN_RUNS)                               AS HIGHEST_SCORE_IN_A_BALL
FROM IPL_MDM.SILVER.CLEAN_DELIVERIES
GROUP BY BATTER
ORDER BY TOTAL_RUNS DESC;

