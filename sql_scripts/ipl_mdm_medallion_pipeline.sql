    MATCH_ID NUMBER,INNING  NUMBER,BATTING_TEAM  VARCHAR(100),
    BOWLING_TEAM VARCHAR(100),OVER   NUMBER,BALL  NUMBER,
    BATTER  VARCHAR(100),BOWLER  VARCHAR(100),NON_STRIKER VARCHAR(100),BATSMAN_RUNS  NUMBER,
    EXTRA_RUNS   NUMBER,TOTAL_RUNS NUMBER,EXTRAS_TYPE  VARCHAR(50),IS_WICKET NUMBER,
    PLAYER_DISMISSED VARCHAR(100),DISMISSAL_KIND  VARCHAR(100),FIELDER   VARCHAR(100)
);

